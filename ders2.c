#include <stdio.h>


int main (){
	
	/*
			x + y = x ile y nin toplamý
			x - y = x den y yi çýkarýr
			x * y = x ile y yi çarpar
			x / y = x ile y yi böler
			
			
			x % y == x ile y nin bölümünden kalaný verir
	*/
	
	int x1 = 1;
	int x2 = -1;
	
	printf("%d\n",12+9);
	printf("%d\n",12-9);
	printf("%d\n",12*9);
	printf("%d\n",12/6);
	printf("%d\n",12%9);
	printf("%.1f\n",12.1+9);
	printf("%d\n",-x1);
	printf("%d\n",x1);
	printf("%d\n",x2);
	printf("%d\n",-x2);





	
	
	
	
	
	return 0;
}
